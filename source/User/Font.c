FontRsltEnum Font_GetMask( FontSizeEnum enFontSize, const unsigned char* pucFont, FontMaskInfo_t* FontMaskInfo )
{
	int iLoop = 0;

	if ( !pucFont || !FontMaskInfo )
	{
		return	FontRslt_Failed;
	}

	if ( FontSize_24 == enFontSize )
	{
		if( *pucFont < 0x80 )
		{
			return	FontRslt_Failed;
		}
		else
		{
			for ( iLoop = 0; iLoop < ARRAY_MAX(astHanZi24x24MaskInfo); ++iLoop )
			{
				if ( (astHanZi24x24MaskInfo[iLoop].acCode[0] == pucFont[0]) \
					&& (astHanZi24x24MaskInfo[iLoop].acCode[1] == pucFont[1]) )
				{
					FontMaskInfo->ucWidth = 24;
					FontMaskInfo->ucHeight = 24;
					FontMaskInfo->ucSize = 72;		/* (24 * 24) / 8 */
					FontMaskInfo->pucMask = astHanZi24x24MaskInfo[iLoop].aucMask;
					return	FontRslt_Success;
				}
			}
		}
	}

	return	FontRslt_Failed;
}

